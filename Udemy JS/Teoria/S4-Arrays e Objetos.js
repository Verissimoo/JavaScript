//Objetos e Arrays

//ARRAYS
//Arrays = adicionar um conjunto de valores a uma unica variavel
//arrays é basicamente uma lista 
//usase: [  ] para declarar
//indice = você pode acessar um valor especifico da lista dizendo a posição dela 
//ferramente length = ve o tamanho do array (e tbm de string)
//Prpiedades = ver o length, acessar o indice
//Metodos = propiedades que funcionam como funções
//diferença de propiedade e metodo é o parentese no final, que qualifica o metodo como um tipo de função

//Metodos de array:
//Push = insere um elemento no array (add no final)
//Pop = remover um elemento no array(do final)
//Shift = remove um elemento no array(no inicio)
//Unshift = insere um elemento no array(no inicio)
//indexOf = encontra um indice(posição) de um determinado elemento
//lastindexOf = encontra o ultimo indice de um elemento 
//Slice = pega e faz uma sublista no array
//forEach = loop com arrays executando parametro por parametro
//includes = procura algum determinado elemento no array
//Reverse = Inverte um array

//Metodos de string:
//Trim = remove tudo que não é string
//padStart = insere caracteres
//Split = transforma uma frase(string) em um array, separando os elementos
//Join = transforma um array em uma string(frase), por meio de um separador
//Repeat = repete a string quantas vezes for declarado

//Rest operator = uma funçao que transforma varios parametros em um array
//Destructuring com objetos = cria variaveis a partir parametros dentro do objeto
//Destructuring em arrays = defini variaveis com valores do array

//JSON = utilizado na comunicação do back end com o front end, semelhante a objetos
